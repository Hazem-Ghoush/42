#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	r;

	if (n / 10 != 0)
		ft_putnbr_fd(n / 10, fd);
	else if (n < 0)
		ft_putchar_fd('-', fd);
	r = n % 10;
	if (r < 0)
		r *= -1;
	ft_putchar_fd(r + '0', fd);
}
